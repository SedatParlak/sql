SELECT
  BRANCH,
  SUM(LINENET) TOTAL_LINENET,
  MIN(LINENET) MIN_LINENET,
  MAX(LINENET) MAX_LINENET,
  AVG(LINENET) AVG_LINENET
FROM 
  SALES
GROUP BY
  BRANCH
HAVING 
  SUM(LINENET) > 50000
ORDER BY 
  TOTAL_LINENET DESC

