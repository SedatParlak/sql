----------------------GROUP BY----------------------

SELECT 
  BRANCH,
  SUM(LINENET) TOTAL_PRICE, 
  COUNT(*) TOTAL_PURCHASE, 
  MIN(PRICE) MIN_PRICE,
  MAX(PRICE) MAX_PRICE,
  AVG(PRICE) AVG_PRICE
FROM 
  SALES
GROUP BY 
  BRANCH
ORDER BY 
  TOTAL_PRICE DESC

