SELECT 
  U.USERNAME_,
  C.CITY,
  T.TOWN,
  D.DISTRICT,
  A.ADDRESSTEXT
FROM 
  USER_ U
JOIN ADDRES A ON A.USERID=U.ID
JOIN CITY C ON C.ID=A.CITYID
JOIN TOWN T ON T.ID=A.TOWNID
JOIN DISTRICT D ON D.ID=A.DISTRICTID